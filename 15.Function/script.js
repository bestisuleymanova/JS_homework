///////////// 1.Return the First Element in an Array.


const arr1 = [1, 2, 3];
const arr2 = [80, 5, 100];
const arr3 = [-500, 0, 50];

const getFirstValue = function (comple) {
  return comple[0];
};
console.log(getFirstValue(arr1));
console.log(getFirstValue(arr2));
console.log(getFirstValue(arr3));

